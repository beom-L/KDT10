## =====================================================================
##                           Tkinter config()/configure() 설정
## =====================================================================
## - 위젯 옵션을 인스턴스 생성 후 변경하거나 확인할 때 쓰는 기본 메서드
## - 
## =====================================================================
## ---------------------------------------------------------------------
## 모듈로딩
## ---------------------------------------------------------------------
import tkinter as tk






## ---------------------------------------------------------------------
## Window 인스턴스 생성 및 폰트 정보 추출 
## ---------------------------------------------------------------------
##- Main Window 인스턴스 생성 및 설정
root = tk.Tk()

##- Button Widget 인스턴스 생성 및 배치
btn = tk.Button(root, text="Click me", bg="lightgray")
btn.pack(padx=10, pady=10)

##- 모든 옵션의 현재/기본값 정보(딕셔너리)
info = btn.configure()     
for opt, meta in list(info.items()):
    print(opt, "→", meta)

##- 인스턴스 생성 후 나중에 옵션/설정 변경
#btn.config(text="눌러봐요", bg="#4A90E2", fg="white")   # = btn.configure(...)
btn.configure(text="눌러봐요", bg="#4A90E2", fg="white")

##- 인스턴스 옵션/설정값 읽기
print(btn.cget("text"))   # => "눌러봐요"
print(btn["bg"])          # => "#4A90E2"


## - --------------------------------------------------------------------
## - 여러개 한꺼번에
## - --------------------------------------------------------------------
##- 여러개 Label Widget 인스턴스 생성 및 배치 
labels = [tk.Label(root, text=f"Item {i}") for i in range(3)]
for w in labels: w.pack()

##- 한꺼번에 여러개 인스턴스 설정 적용
for w in labels:
    w.config(bg="#E3F2FD", fg="#0D47A1", padx=6, pady=4)

## ---------------------------------------------------------------------
## Window 이벤트 루프
## ---------------------------------------------------------------------
root.mainloop()
